package com.bluemobi.product.model;

import com.bluemobi.product.model.common.BasicModel;

/**
 * 基本查询对象
 * 
 * @version V1.0
 * @author Administrator
 * @date 2014年8月22日
 */
public class BasicSearchModel extends BasicModel {

	/**
	 * serial Version UID
	 */
	private static final long serialVersionUID = -2975036005508092381L;

	/** 经度 */
	private String longitude;
	/** 纬度 */
	private String latitude;
	/** 查询关键字 */
	private String searchKey = "";

	/**
	 * 经度的取得。
	 * 
	 * @return 经度
	 */
	public String getLongitude() {
		return longitude;
	}

	/**
	 * 经度的设定。
	 * 
	 * @param pLongitude
	 *            经度
	 */
	public void setLongitude(String pLongitude) {
		this.longitude = pLongitude;
	}

	/**
	 * 纬度的取得。
	 * 
	 * @return 纬度
	 */
	public String getLatitude() {
		return latitude;
	}

	/**
	 * 纬度的设定。
	 * 
	 * @param pLatitude
	 *            纬度
	 */
	public void setLatitude(String pLatitude) {
		this.latitude = pLatitude;
	}

	/**
	 * 查询关键字的取得。
	 * 
	 * @return 查询关键字
	 */
	public String getSearchKey() {
		return searchKey;
	}

	/**
	 * 查询关键字的设定。
	 * 
	 * @param pSearchKey
	 *            查询关键字
	 */
	public void setSearchKey(String pSearchKey) {
		this.searchKey = pSearchKey;
	}

}
