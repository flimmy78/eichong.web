package com.bluemobi.product.service;

import java.util.List;

import com.bluemobi.product.model.MenuRoleModel;

/**
 * 菜单角色权限业务处理接口
 * 
 * @version V1.0
 * @author Administrator
 * @date 2014年6月26日
 */
public interface MenuRoleService {

	/**
	 * 取得菜单角色权限限信息
	 * 
	 * @author yangweir
	 * @since Version 1.0
	 * @param menuRoleModel
	 *            菜单角色权限限对象
	 * @return 无
	 * @throws 无
	 */
	public MenuRoleModel findMenuRole(MenuRoleModel menuRoleModel);

	/**
	 * 取得菜单角色权限限一览
	 * 
	 * @author yangweir
	 * @since Version 1.0
	 * @return List<MenuRole> 菜单角色权限限一览
	 * @throws 无
	 */
	public List<MenuRoleModel> getMenuRoleList();

	/**
	 * 添加菜单角色权限限
	 * 
	 * @author yangweir
	 * @since Version 1.0
	 * @param MenuRoleModel
	 *            菜单角色权限限信息
	 * @return MenuRole 菜单角色权限限信息
	 * @throws 无
	 */
	public MenuRoleModel addMenuRole(MenuRoleModel menuRoleModel);

	/**
	 * 编辑菜单角色权限限
	 * 
	 * @author yangweir
	 * @since Version 1.0
	 * @param MenuRoleModel
	 *            菜单角色权限限信息
	 * @return String 处理结果标识
	 * @throws 无
	 */
	public String modifyMenuRole(MenuRoleModel menuRoleModel);

	/**
	 * 删除菜单角色权限限
	 * 
	 * @author yangweir
	 * @since Version 1.0
	 * @param menuRoleModel
	 *            菜单角色权限限对象
	 * @return 无
	 * @throws 无
	 */
	public void removeMenuRole(MenuRoleModel menuRoleModel);

}
