package com.bluemobi.product.service;

import java.util.List;

import com.bluemobi.product.model.TableItemModel;

/**
 * 数据库表字段定义业务处理接口
 * 
 * @version V1.0
 * @author Administrator
 * @date 2014年6月26日
 */
public interface TableItemService {

	/**
	 * 取得数据库表字段信息
	 * 
	 * @author yangweir
	 * @since Version 1.0
	 * @param tableItemModel
	 *            数据库表字段对象
	 * @return 无
	 * @throws 无
	 */
	public TableItemModel findTableItem(TableItemModel tableItemModel);

	/**
	 * 取得数据库表字段一览
	 * 
	 * @author yangweir
	 * @since Version 1.0
	 * @return List<TableItem> 数据库表字段一览
	 * @throws 无
	 */
	public List<TableItemModel> getTableItemList();

	/**
	 * 添加数据库表字段
	 * 
	 * @author yangweir
	 * @since Version 1.0
	 * @param TableItemModel
	 *            数据库表字段信息
	 * @return TableItem 数据库表字段信息
	 * @throws 无
	 */
	public TableItemModel addTableItem(TableItemModel tableItemModel);

	/**
	 * 编辑数据库表字段
	 * 
	 * @author yangweir
	 * @since Version 1.0
	 * @param TableItemModel
	 *            数据库表字段信息
	 * @return String 处理结果标识
	 * @throws 无
	 */
	public String modifyTableItem(TableItemModel tableItemModel);

	/**
	 * 删除数据库表字段
	 * 
	 * @author yangweir
	 * @since Version 1.0
	 * @param tableItemModel
	 *            数据库表字段对象
	 * @return 无
	 * @throws 无
	 */
	public void removeTableItem(TableItemModel tableItemModel);

}
