package com.wanma.model;

import java.util.Date;

import com.bluemobi.product.model.common.BasicListAndMutiFile;
import com.bluemobi.product.model.common.BasicModel;

/**
 * 常见问题表
 * 
 * @author xiay
 *
 */
public class CommonQuestions extends BasicListAndMutiFile{
	
	/**
	 * serial Version UID
	 */
	private static final long serialVersionUID = 1L;

	private String questionId; //主键ID
	
	private String questionName; //常见问题
	
	private String replyContent; //回复内容
	
	private Date createDate; //创建时间
	
	private String updateDate; //修改时间

	public String getQuestionId() {
		return questionId;
	}

	public void setQuestionId(String questionId) {
		this.questionId = questionId;
	}

	public String getQuestionName() {
		return questionName;
	}

	public void setQuestionName(String questionName) {
		this.questionName = questionName;
	}

	public String getReplyContent() {
		return replyContent;
	}

	public void setReplyContent(String replyContent) {
		this.replyContent = replyContent;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public String getUpdateDate() {
		return updateDate;
	}

	public void setUpdateDate(String updateDate) {
		this.updateDate = updateDate;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	
	
}
