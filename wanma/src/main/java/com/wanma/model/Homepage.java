package com.wanma.model;

import com.bluemobi.product.model.common.BasicListAndMutiFile;

/**
 * 首页轮播图
 * 
 * @author xiay
 *
 */
public class Homepage extends BasicListAndMutiFile{
	
	/**
	 * serial Version UID
	 * 
	 */
	private static final long serialVersionUID = 1L;
	
	private String homepageId;   //主键ID
	
	private String homePageName;  //首页广告名称
	
	private String homePageImage;  //首页图片
	
	private String homePageSequence;  //图片轮播顺序
	
	private String Createdate; //创建时间
	
	private String Updatedate;  //修改时间按
	
	private String homePageStatus;  //状态
	private String homePageUrl;//链接

	public String getHomepageId() {
		return homepageId;
	}

	public void setHomepageId(String homepageId) {
		this.homepageId = homepageId;
	}

	public String getHomePageName() {
		return homePageName;
	}

	public void setHomePageName(String homePageName) {
		this.homePageName = homePageName;
	}

	public String getHomePageImage() {
		return homePageImage;
	}

	public void setHomePageImage(String homePageImage) {
		this.homePageImage = homePageImage;
	}

	public String getHomePageSequence() {
		return homePageSequence;
	}

	public void setHomePageSequence(String homePageSequence) {
		this.homePageSequence = homePageSequence;
	}

	public String getCreatedate() {
		return Createdate;
	}

	public void setCreatedate(String createdate) {
		Createdate = createdate;
	}

	public String getUpdatedate() {
		return Updatedate;
	}

	public void setUpdatedate(String updatedate) {
		Updatedate = updatedate;
	}

	public String getHomePageStatus() {
		return homePageStatus;
	}

	public void setHomePageStatus(String homePageStatus) {
		this.homePageStatus = homePageStatus;
	}

	public static long getSerialversionuid() {
		return serialVersionUID;
	}

	public String getHomePageUrl() {
		return homePageUrl;
	}

	public void setHomePageUrl(String homePageUrl) {
		this.homePageUrl = homePageUrl;
	}

	@Override
	public String toString() {
		return "Homepage [homepageId=" + homepageId + ", homePageName="
				+ homePageName + ", homePageImage=" + homePageImage
				+ ", homePageSequence=" + homePageSequence + ", Createdate="
				+ Createdate + ", Updatedate=" + Updatedate
				+ ", homePageStatus=" + homePageStatus + ", homePageUrl="
				+ homePageUrl + "]";
	}
	
	
}
